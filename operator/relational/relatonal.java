import java.util.Scanner;
class  relational
{
	public static void main(String[] args) 
	{
		System.out.println("Retional operator");
		
        int a = 10;
        int b = 20;

        System.out.println("Values: a = " + a + ", b = " + b);

        // Equal to (==)
        System.out.println("a == b : " + (a == b));

        // Not equal to (!=)
        System.out.println("a != b : " + (a != b));

        // Greater than (>)
        System.out.println("a > b : " + (a > b));

        // Less than (<)
        System.out.println("a < b : " + (a < b));

        // Greater than or equal to (>=)
        System.out.println("a >= b : " + (a >= b));

        // Less than or equal to (<=)
        System.out.println("a <= b : " + (a <= b));
    }
}

	
